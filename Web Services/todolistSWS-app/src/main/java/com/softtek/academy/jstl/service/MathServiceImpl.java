package com.softtek.academy.jstl.service;

public class MathServiceImpl {
	
	public long sum (long num1, long num2) {
		return num1+num2;
	}
	public long res (long num1, long num2) {
		return num1-num2;
	}
	public long mult (long num1, long num2) {
		return num1*num2;
	}
	public long div (long num1, long num2) {
		return num1/num2;
	}
}
