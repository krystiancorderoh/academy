package com.softtek.academy.java.basics.operators;

public class Intrement {

	public static void main(String[] args) {
		
		int x = 5;
		x = x + 1;
		
		//x++
		//++x
		
		System.out.println(x++);
		System.out.println(++x);

		//x--
				//--x
				
				System.out.println(x--);
				System.out.println(--x);
		
		x+=5; // x = x + 5
		

	}

}
