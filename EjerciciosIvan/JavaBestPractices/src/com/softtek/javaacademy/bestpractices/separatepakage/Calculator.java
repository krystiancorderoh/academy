package com.softtek.javaacademy.bestpractices.separatepakage;

public class Calculator {
	
	public int additionOfTwoNumbers(int num1, int num2) {
		return num1 + num2;
	}
	
	public int substractionOfTwoNumbers(int num1, int num2) {
		 return num1 - num2;
	}
	
	public int multiplicationOfTwoNumbers(int num1, int num2) {
		return num1 * num2;
	}
	
	public double divisionOfNumbers(int num1, int num2) {
		return (double)num1 / (double)num2;
	}
	
	
}
